import json
import random
import torch
from io import BytesIO
from PIL import Image
from kornia.augmentation import AugmentationBase2D
from torchvision.transforms.functional import to_pil_image
from torchvision.io import decode_jpeg
from typing import Union


__all__ = ["RandomSocialMediaCompression", "DEFAULT_QTABLE_JSON"]


class RandomSocialMediaCompression(AugmentationBase2D):
    def __init__(
        self,
        qtable_json=None,
        p: float = 1.0,
        same_on_batch: bool = False,
        debug: bool = False,
        return_platform: bool = False,
    ):
        super().__init__(p=p, same_on_batch=same_on_batch, p_batch=p)
        self.debug = debug
        if qtable_json is not None:
            if isinstance(qtable_json, str):
                self.qtable_json = json.load(open(qtable_json))
            elif isinstance(qtable_json, dict):
                self.qtable_json = qtable_json
            else:
                raise ValueError("qtable_json should be a path to a json file or a dictionary")
        else:
            self.qtable_json = DEFAULT_QTABLE_JSON
        self.sm_pltfms = list(self.qtable_json.keys())
        self.return_platform = return_platform

    def return_result(self, x, return_platform=None):
        if self.return_platform:
            return {
                "image": x,
                "platform": return_platform if return_platform is not None else "UNKNOWN",
            }
        else:
            return x

    def recompress(self, x: Union[Image.Image, torch.Tensor], qtables, pltfm_profile):
        if isinstance(x, torch.Tensor):
            x = to_pil_image(x)
        qtables = [qtables["0"], qtables["1"]]
        buffer = BytesIO()
        x.save(buffer, format="JPEG", qtables=qtables)
        x = decode_jpeg(torch.frombuffer(buffer.getbuffer(), dtype=torch.uint8)).float() / 255
        return self.return_result(x, pltfm_profile)

    def choose_qtables(self):
        pltfm = random.choice(self.sm_pltfms)
        profile = random.choice(("Post", "Profile"))
        qtables = random.choice(self.qtable_json[pltfm][profile])
        if self.debug:
            print(f"prob: {self.p}, platform: {pltfm}, profile: {profile}, qtables: {qtables}")
        return qtables, f"{pltfm}_{profile}"

    def forward(self, x: torch.Tensor, params=None, **kwargs) -> torch.Tensor:
        if random.random() > self.p:
            return self.return_result(x)
        qtables, pltfm_profile = self.choose_qtables()
        return self.recompress(x, qtables, pltfm_profile)


